#include <iostream>
using namespace std;

int main() {

    cout<<"The BitWise & is: "<<(6 & 10)<<endl;
    cout<<"The BitWise | is: "<<(6 | 10)<<endl;
    cout<<"The BitWise ^ is: "<<(6 ^ 10)<<endl;
    cout<<"The BitWise << is: "<<(10 << 2)<<endl;
    cout<<"The BitWise >> is:"<<(10 >> 1)<<endl;

    return 0;
}
